def handle_unknown_input(user_text: str, user: Any):
    text = (user_text or "").lower()

    # Эмоциональный fallback: сначала стабилизация, потом возврат в действие.
    if any(word in text for word in ["трев", "страш", "паник", "не могу", "плохо"]):
        return (
            "Похоже, тебе сейчас непросто.\n\n"
            "Давай не будем усложнять.\n"
            "Сделай одну вещь:\n\n"
            "👉 посмотри вокруг и назови 3 предмета\n\n"
            "После этого нажми кнопку ниже.",
            ["💪 Давай тренировать", "🆘 Кризис"],
        )

    return (
        "Я понял.\n\n"
        "Давай вернёмся к шагу и продолжим.",
        ["💪 Давай тренировать", "🧠 Разобрать"],
    )

_CLARIFY_THRESHOLD = 8  # слов меньше этого — просим уточнить


def need_clarify(text: str) -> bool:
    """Return True if the text is too short or vague to analyse."""
    words = text.split()
    return len(words) < _CLARIFY_THRESHOLD


def clarify_question(mode: str = "normal") -> str:
    """Return a clarifying question styled for the given mode."""
    return _CLARIFY_QUESTIONS.get(mode, _CLARIFY_QUESTIONS["normal"])


def render_behavior_chain(items: list) -> str:
    """Render a simple numbered behaviour chain from a list of strings."""
    return "\n".join(f"{i + 1}. {item}" for i, item in enumerate(items))


