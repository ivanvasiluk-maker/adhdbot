def trainer_block(trainer_key: str, block_name: str):
    """Get responses for a trainer and pattern block."""
    trainer_key = trainer_key if trainer_key in RESPONSES else "marsha"
    return RESPONSES[trainer_key].get(block_name)


def detect_dialog_pattern(text: str) -> Optional[str]:
    """Detect a resistance pattern in free text. Returns the pattern key or None."""
    low = text.lower()
    for pattern, keywords in PATTERN_KEYWORDS.items():
        for kw in keywords:
            if kw in low:
                return pattern
    return None


def get_dialog_reply(trainer_key: str, mode: str, pattern: str) -> Optional[str]:
    """Return a random reply for the given trainer, mode and pattern."""
    trainer_key = trainer_key if trainer_key in RESPONSES else "marsha"
    mode = mode if mode in ("easy", "normal", "hard") else "normal"
    options = RESPONSES.get(trainer_key, {}).get(pattern, {}).get(mode)
    if not options:
        return None
    return random.choice(options)


